	$('.map circle:not(.flyport)').click(function (e) {
		$('.info-panel').remove();
		var region_code = $(this).attr('id').toLowerCase();
		var locations = "";
		$.each(cities, function (index, value) {
			if (value["code"] === region_code) {
				$('.locName').text(value["city_name"]);				
				$('.population').text(value["population"]);
				
				$('.important-locations').empty();
				$.each(value["important_loc"], function (index, value) {
					
					$('.important-locations').append("<div><p>" + value["location_name"] + "</p></div>");
				});
			}
		});
	});
	
